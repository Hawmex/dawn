class _Named with Color {
  final String name;

  const _Named(this.name);

  @override
  String toString() => name;
}

class RgbColor with Color {
  final int red;
  final int green;
  final int blue;
  final double alpha;

  const RgbColor(
    final this.red,
    final this.green,
    final this.blue, [
    final this.alpha = 1,
  ])  : assert(red >= 0 && red <= 255),
        assert(green >= 0 && green <= 255),
        assert(blue >= 0 && blue <= 255),
        assert(alpha >= 0 && alpha <= 1);

  RgbColor withAlpha(final double alpha) {
    assert(alpha >= 0 && alpha <= 1);
    return RgbColor(red, green, blue, alpha);
  }

  @override
  String toString() => 'rgb($red $green $blue / $alpha)';
}

class HexColor with Color {
  final int hex;

  const HexColor(this.hex) : assert(hex >= 0x00000000 && hex <= 0xFFFFFFFF);

  HexColor withAlpha(final int alpha) {
    assert(alpha >= 0x0 && alpha <= 0xFF);
    return HexColor((hex ~/ 0xFF) * 0xFF + alpha);
  }

  @override
  String toString() => '#${hex.toRadixString(16)}';
}
